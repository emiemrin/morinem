package morinem.at02;
public class At02Ex05 {

    public static void main(String[] args) {
        
        
        for(char ch='A';ch<='Z';++ch)
        {
            System.out.print(ch + " ");
        }
        System.out.println("\n");
        for(char ch='Z';ch>='A';--ch)
        {
            System.out.print(ch + " ");
        }
}
}
